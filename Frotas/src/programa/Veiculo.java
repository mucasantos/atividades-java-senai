package programa;

public class Veiculo {
	protected int chassi;
	protected int pesoBruto;
	protected int pesoSemiReboque;
	protected int pesoOutros;
	protected int velocidadeIda;
	protected int velocidadeVolta;
	
	public Veiculo() {}
	
	public Veiculo(int chassi, int pesoBruto, int pesoSemiReboque, int pesoOutros, int velocidadeIda,
			int velocidadeVolta) {
		super();
		this.chassi = chassi;
		this.pesoBruto = pesoBruto;
		this.pesoSemiReboque = pesoSemiReboque;
		this.pesoOutros = pesoOutros;
		this.velocidadeIda = velocidadeIda;
		this.velocidadeVolta = velocidadeVolta;
	}




	
	
	
	
	
}